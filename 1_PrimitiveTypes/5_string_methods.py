my_name = "Kent Lexter Co"
print(my_name.upper())
print(my_name.lower())
print(my_name.title())  # capitalize first letter of word
print(my_name.strip())  # trim white space on beginning and end
print(my_name.lstrip())  # trim white space on left side
print(my_name.rstrip())  # trim white space on right side
print(my_name.find("Co"))  # grabs the index within the string (Case sensitive)
print(my_name.replace("e", "a"))
print("e" in my_name)  # Returns T/F
print("z" not in my_name)  # Returns T/F
